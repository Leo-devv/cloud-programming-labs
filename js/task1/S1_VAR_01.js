// S1_VAR_01 - Declare & observe

var userName = "Leo";
let userAge = 25;
const isActive = true;

// print a quick table showing each variable
console.log("Variable     Keyword  Value      typeof");
console.log("------------------------------------------");
console.log(`userName     var      ${userName}        ${typeof userName}`);
console.log(`userAge      let      ${userAge}         ${typeof userAge}`);
console.log(`isActive     const    ${isActive}       ${typeof isActive}`);
